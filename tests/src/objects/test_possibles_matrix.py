@pytest.mark.parametrize(
    "rows,cols,numbers",
    [
        (range(0, 3), range(0, 3), range(0, 9)),
        (range(3, 6), range(0, 3), range(0, 9)),
        (range(6, 9), range(0, 3), range(0, 9)),
        (range(0, 3), range(3, 6), range(0, 9)),
        (range(3, 6), range(3, 6), range(0, 9)),
        (range(6, 9), range(3, 6), range(0, 9)),
        (range(0, 3), range(6, 9), range(0, 9)),
        (range(3, 6), range(6, 9), range(0, 9)),
        (range(6, 9), range(6, 9), range(0, 9)),
    ],
)
def test_setsqrs_works_properly(rows, cols, numbers):
    # GIVEN
    possibles = PossiblesMatrix()

    # WHEN
    for row in rows:
        for col in cols:
            for number in numbers:
                possibles.__setsqrs__(np.array([row]), np.array([col]), np.array([number]), 0)

                # THEN
                assert np.all(possibles[to_slice(rows), to_slice(cols), number] == 0)


def test_get_not_empty_items_work_properly():
    # GIVEN
    possibles = PossiblesMatrix()
    expected_numbers = np.array([1, 2, 3, 4, 5, 6, 9])

    # WHEN
    possibles[0, 0] = np.array([1, 2, 3, 4, 0, 0, 5, 6, 9])

    # THEN
    assert np.all(possibles.get_not_empty_items(0, 0) == expected_numbers)


def to_slice(_range):
    return slice(_range.start, _range.stop)
